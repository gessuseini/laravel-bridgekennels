        document.addEventListener('DOMContentLoaded', function() {
            const faqHeaders = document.querySelectorAll('.ui-accordion-header');

            // Initially close all FAQ items except the first one
            faqHeaders.forEach((header, index) => {
                if (index !== 0) {
                    header.nextElementSibling.style.display = 'none';
                    header.setAttribute('aria-expanded', 'false');
                    header.querySelector('.ui-accordion-header-icon').classList.add('template-arrow-circle-right');
                }
            });

            faqHeaders.forEach(header => {
                header.addEventListener('click', function(event) {
                    event.preventDefault(); // Prevent default behavior
                    event.stopPropagation(); // Stop event from bubbling up

                    const isActive = header.classList.contains('ui-accordion-header-active');

                    // Close all other headers except the clicked one
                    faqHeaders.forEach(otherHeader => {
                        if (otherHeader !== header) {
                            otherHeader.classList.remove('ui-accordion-header-active', 'ui-state-active');
                            otherHeader.nextElementSibling.style.display = 'none';
                            otherHeader.setAttribute('aria-expanded', 'false');
                            otherHeader.querySelector('.ui-accordion-header-icon').classList.add('template-arrow-circle-right');
                        }
                    });

                    // Toggle the clicked header
                    if (!isActive) {
                        header.classList.add('ui-accordion-header-active', 'ui-state-active');
                        header.nextElementSibling.style.display = 'block';
                        header.setAttribute('aria-expanded', 'true');
                        header.querySelector('.ui-accordion-header-icon').classList.remove('template-arrow-circle-right');
                        header.querySelector('.ui-accordion-header-icon').classList.add('template-arrow-circle-down');
                    }
                });
            });
        });
    </script>

